-- Verify the insert
SELECT 
    COUNT(*) as total_providers,
    COUNT(CASE WHEN is_active = true THEN 1 END) as active_providers,
    COUNT(CASE WHEN is_active = false THEN 1 END) as inactive_providers,
    COUNT(CASE WHEN provider_type = 'individual' THEN 1 END) as individual_providers,
    COUNT(CASE WHEN provider_type = 'organization' THEN 1 END) as organization_providers
FROM providers;

-- Display all inserted providers
SELECT 
    first_name || ' ' || last_name as name,
    npi,
    credentials,
    taxonomy_specialty,
    practice_for_provider,
    is_active,
    created_at
FROM providers
ORDER BY created_at DESC;

