-- Verify the insert
SELECT 
    COUNT(*) as total_practices,
    COUNT(CASE WHEN status = 'active' THEN 1 END) as active_practices,
    COUNT(CASE WHEN status = 'pending' THEN 1 END) as pending_practices,
    COUNT(CASE WHEN status = 'inactive' THEN 1 END) as inactive_practices,
    SUM(provider_count) as total_providers,
    SUM(patient_count) as total_patients,
    SUM(monthly_revenue) as total_monthly_revenue
FROM practices;

-- Display all inserted practices
